/**
 * Dish 데이터 생성 함수
 */
function generateDishes(): Dish[] {
	const dishes: Dish[] = [];
	let dishId = 1;

	// ingredientId와 등급 매핑
	const gradeMap: Record<number, string> = {};

	// G등급 (1-23)
	for (let i = 1; i <= 8; i++) gradeMap[i] = 'G';
	for (let i = 9; i <= 23; i++) gradeMap[i] = 'G';

	// F등급 (101-172)
	for (let i = 101; i <= 112; i++) gradeMap[i] = 'F';
	gradeMap[123] = 'F';
	for (let i = 124; i <= 172; i++) gradeMap[i] = 'F';

	// E등급 (201-248)
	for (let i = 201; i <= 214; i++) gradeMap[i] = 'E';
	for (let i = 215; i <= 248; i++) gradeMap[i] = 'E';

	// D등급 (301-340)
	for (let i = 301; i <= 313; i++) gradeMap[i] = 'D';
	for (let i = 314; i <= 340; i++) gradeMap[i] = 'D';

	// C등급 (401-432)
	for (let i = 401; i <= 412; i++) gradeMap[i] = 'C';
	for (let i = 413; i <= 432; i++) gradeMap[i] = 'C';

	// B등급 (501-529)
	for (let i = 501; i <= 511; i++) gradeMap[i] = 'B';
	for (let i = 512; i <= 529; i++) gradeMap[i] = 'B';

	// A등급 (601-628)
	for (let i = 601; i <= 612; i++) gradeMap[i] = 'A';
	for (let i = 613; i <= 628; i++) gradeMap[i] = 'A';

	// 각 ingredientId에 대해 대성공/실패 Dish 생성
	for (const [idStr, grade] of Object.entries(gradeMap)) {
		const ingredientId = parseInt(idStr);
		const failName = FAIL_NAMES[ingredientId];

		if (!failName) continue;

		// 대성공 Dish
		dishes.push({
			id: dishId++,
			ingredientId,
			name: '', // 동적으로 생성 (수식어 + 원래 이름)
			resultType: 'critical',
			probability: 5
		});

		// 실패 Dish
		dishes.push({
			id: dishId++,
			ingredientId,
			name: failName,
			resultType: 'fail',
			probability: FAIL_PROBABILITY_BY_GRADE[grade] || 10
		});
	}

	return dishes;
}

/**
 * Dish 데이터
 * - 261개 재료/요리 × 2 (대성공 + 실패) = 522개
 */
export const DISHES_DATA: Dish[] = generateDishes();
